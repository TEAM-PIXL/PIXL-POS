package teampixl.com.pixlpos.application;

/*===================================================================================================================================================================================
--------------------------------------------------------->   Launcher Class for POS Application   <-----------------------------------------------------------------------------------
====================================================================================================================================================================================*/

/**
 * Launcher class for the POS Application.
 * This class contains the main method which serves as the entry point for the application.
 * It delegates the launch process to the POSApplication class.
 */
public class POSApplicationLauncher {

    /**
     * The main method to launch the POS Application.
     *
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        POSApplication.main(args);
    }
}
